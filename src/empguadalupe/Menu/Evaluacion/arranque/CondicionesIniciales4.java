     public Boolean generarpreguntas(int pos) {
        Boolean resultado = false;
        Metodos generar = new Metodos();
        Stack<Integer> numero = generar.generaNumeroAleatoriosin(1, 5);
        traertodo();
        int selop[] = new int[4];
        selop[0] = pos;
        selop[1] = numero.get(0);
        selop[2] = numero.get(1);
        selop[3] = numero.get(2);
        int acert0 = 0;
        int acertar1, acertar2, acertar3, acertar4;

        if (selop[1] == pos || selop[2] == pos || selop[3] == pos) {
            selop[0] = pos;
            if (selop [1]==pos){
            selop[1] = numero.get(3);
            }
            if (selop [2]==pos){
            selop[2] = numero.get(3);
            }
            if (selop [3]==pos){
            selop[3] = numero.get(3);
            }
            
            Stack<Integer> pila = generar.generaNumeroAleatorio(1, 4);
            String[] options = new String[]{textos[selop[pila.get(0)]], textos[selop[pila.get(1)]], textos[selop[pila.get(2)]], textos[selop[pila.get(3)]]};
            int response = JOptionPane.showOptionDialog(null, "cual de las condiciones iniciales falta", "Title",
                    JOptionPane.DEFAULT_OPTION, JOptionPane.PLAIN_MESSAGE,
                    null, options, options[0]);
            acertar1 = pila.get(0);
            acertar2 = pila.get(1);
            acertar3 = pila.get(2);
            acertar4 = pila.get(3);
            if (acertar1 == 0) {
                acert0 = 0;
            }
            if (acertar2 == 0) {
                acert0 = 1;
            }
            if (acertar3 == 0) {
                acert0 = 2;
            }
            if (acertar4 == 0) {
                acert0 = 3;
            }
            if (response == acert0) {
                JOptionPane.showMessageDialog(rootPane, "correcto");
           
                resultado = true;

            } else {
                JOptionPane.showMessageDialog(rootPane, "incorrecto");
                contErrores=contErrores+1;
                resultado = false;
            }
        } else {

            Stack<Integer> pila = generar.generaNumeroAleatorio(1, 4);
            String[] options = new String[]{textos[selop[pila.get(0)]], textos[selop[pila.get(1)]], textos[selop[pila.get(2)]], textos[selop[pila.get(3)]]};
            int response = JOptionPane.showOptionDialog(null, "cual de las condiciones iniciales falta", "Title",
                    JOptionPane.DEFAULT_OPTION, JOptionPane.PLAIN_MESSAGE,
                    null, options, options[0]);
            acertar1 = pila.get(0);
            acertar2 = pila.get(1);
            acertar3 = pila.get(2);
            acertar4 = pila.get(3);
            if (acertar1 == 0) {
                acert0 = 0;
            }
            if (acertar2 == 0) {
                acert0 = 1;
            }
            if (acertar3 == 0) {
                acert0 = 2;
            }
            if (acertar4 == 0) {
                acert0 = 3;
            }
            if (response == acert0) {
                JOptionPane.showMessageDialog(rootPane, "correcto");
              
                resultado = true;
            } else {
                JOptionPane.showMessageDialog(rootPane, "incorrecto");
                contErrores=contErrores+1;
                resultado = false;
                 
            }
        }

        return resultado;
    }
 

