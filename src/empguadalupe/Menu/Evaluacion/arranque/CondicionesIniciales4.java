    public String[] textos(int numero1, int i) {
        String labels[] = new String[5];

        switch (numero1) {
            case 1:
                labels[i] = la1.getText();
                la1.setForeground(Color.BLACK);
                select[1] = true;

                break;
            case 2:
                labels[i] = la2.getText();
                la2.setForeground(Color.black);
                select[2] = true;
                break;
            case 3:
                labels[i] = la3.getText();
                la3.setForeground(Color.BLACK);
                select[3] = true;
                break;
            case 4:
                labels[i] = la4.getText();
                la4.setForeground(Color.BLACK);
                select[4] = true;
                break;
   
        }
        return labels;
    }
